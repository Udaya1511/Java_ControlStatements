package loops_Statement;

public class Advanced_ForLoop {
	
	public static void main(String[] args) {
		
		String[] name = {"Java", "C", "C++", "Python"};
		
		System.out.println("Printing the content of the array names:\n");
		
		for (String nn : name) {
			
			System.out.println(nn);
			
		}
	}

}
